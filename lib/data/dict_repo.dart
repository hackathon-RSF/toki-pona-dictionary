class Entry {
  final String word;
  final List<Meaning> meanings;
  final Origin? origin;
  Entry({required this.word, required this.meanings, this.origin});

  @override
  String toString() =>
      'Entry(word: $word, meanings: $meanings, origin: $origin)';
}

class Meaning {
  final List<String> translations;
  final Usage usage;
  Meaning({required this.translations, required this.usage});

  @override
  String toString() =>
      'Translations: ${translations.join(", ")}\nUsage: ${usage.name}';
}

class Origin {
  final String word;
  final String language;
  final String? meaning;
  Origin({required this.word, required this.language, this.meaning});

  @override
  String toString() => 'Word: $word ($language), Meaning: $meaning';
}

enum Usage {
  interject,
  adjective,
  noun,
  verb,
  particle,
  number,
  preverb,
  preposition,
}
